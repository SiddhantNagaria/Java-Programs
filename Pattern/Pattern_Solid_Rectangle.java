public class Pattern_Solid_Rectangle {
    public static void main(String[] args) {
        int n = 4;
         int m = 5;
         int i , j;
        for(i = 1; i <= n; i++)
        {
            for(j = 1; j <= m; j++)
            {
                System.out.print("* ");
            }
            System.out.print("\n");
        }
    }
}
